import UIKit

// COLECCIONES
/* 
 - ARRAYS = COLECCIONES ORDENADAS (Sin espacios vacios, los valores se pueden repetir) (Solo acepta un tipo de dato - el que se define)
 - SET = COLECCIONES NO ORDENADAS (Pero tiene valores unicos no se podrán repetir)
 - DICCIONARIOS = Se almacenan datos con el formato clave valor , cada valor tendrá un identificador (único) para acceder al mismo
*/
//Forma de escribir un ARRAY
Array <String>()
[String]()

// Forma de escribir un SET
Set <String>()

// Forma de escribir un DICTIONARY (clave , valor)
Dictionary <Int,String>()
